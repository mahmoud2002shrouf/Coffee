        if (gRATE1.getSelectedIndex() == 0) {
            grader = 1;

        } else if (gRATE1.getSelectedIndex() == 1) {
            grader = 2;

        } else {
            grader = 3;

        }
        if (bEANS2.getSelectedIndex() == 0) { 

            Type = 1;
            CupCoffee cupCoffe = new CupCoffee("Arabica", "Espresso", "single", grader, c1.getWater().getLevleOfWater(), c1.getBean().type1.levle, c1.getBean().type2.levle);
            cup.add(cupCoffe);
            Arya.append(cupCoffe.toString() + "\n");

        } else {

            Type = 2;
            CupCoffee cupCoffe = new CupCoffee("Robusta", "Espresso", "single", grader, c1.getWater().getLevleOfWater(), c1.getBean().type1.levle, c1.getBean().type2.levle);

            cup.add(cupCoffe);
            Arya.append(cupCoffe.toString() + "\n");

        }

        try {
        
            c1.MakeCoffee(Type, 1, 1, grader);
 
        } catch (OutOfWaterException | InterruptedException e) {
            System.out.println("Sorry, the machine is out of water.");
        } catch (OutOfBeansException ex) {
            System.out.println("Sorry, the machine is out of BEANS.");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(formCoffee.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(formCoffee.class.getName()).log(Level.SEVERE, null, ex);
        }
