    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int Water = Integer.parseInt(Add.getText());
        if (Water < 0) {
            JOptionPane.showMessageDialog(null, "woring enter");
        } else if (Water + c1.getWater().getLevleOfWater() > c1.getWater().getCapacity()) {
            JOptionPane.showMessageDialog(null, "There is not enough space");

        } else {
            c1.getWater().fillTank(Water);
            Area2.append("add water " + Water + " \n");
        }

        String water = c1.getWater().toString();
        waterTank.setText(water);
        String ar = c1.getBean().toString1();
        Atank1.setText(ar);
        String rb = c1.getBean().toString2();
        Rtank.setText(rb);
        Clean.setText(c1.count_cup + "");
        Add.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

